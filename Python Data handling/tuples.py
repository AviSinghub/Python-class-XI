# Tuples: Tuples are lists whose values can't be changed, means aren't modifiable.
#         Tuples are represented by comma-separated values of any data types within parentheses
# Tuple are immutable, unchangable
# one can't change values of tuple.
# Tuples are used to store multiple items in a single variable.
# Tuple is one of 4 built-in data types in Python used to store collections of data, the other 3 are List, Set, and Dictionary, all with different qualities and usage.
 
tupleA = ( 2,4,6,8,9,10,12,14,16,18,20)
tupleB = ('s','f', 'a','j','g')
print(tupleA,'\n')
print(tupleB,'\n')

print("Length of the tupleB:",len(tupleB))        # len() function is use to get the length of tuple.
print("Length of the tupleA:",len(tupleA))        # len() function is use to get the length of tuple.